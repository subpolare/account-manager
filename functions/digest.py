    lines = [
        '@subpolare @johnnywhale',
        f'‚òÄÔ∏è {greeting}\n\n*–°–µ–≥–æ–¥–Ω—è –≤–∞–º –ø—Ä–µ–¥—Å—Ç–æ–∏—Ç –æ–ø—É–±–ª–∏–∫–æ–≤–∞—Ç—å üíé {len(items)} –µ–¥–∏–Ω–∏—Ü{"—ã" if len(items) <= 4 else ""} –∫–æ–Ω—Ç–µ–Ω—Ç–∞*',
        ''
    ]
    for i, (place, text) in enumerate(items, start=1):
        text = text.replace('\n', ' ')
        lines.append(f'{i}. {place}: {text}')
    return '\n'.join(lines)

